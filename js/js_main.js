
function handle_link(index) {
    switch (index) {
        case 0:
            window.open("about.html", "_parent");
            break;
        case 1:
            window.open("https://github.com/villeruokonen?tab=repositories");
            break;
        case 2: //linktree link index
            window.open("https://linktr.ee/villeruo");
            break;
        case 3: //linktree link index
            window.open("https://linkedin.com/in/villeruo");
            break;
        case 4:
            window.open("main_html.html");
            break;
        case 5:
            window.open("https://docs.google.com/document/d/17xFSdhFWHwWpOze68DK5_Fn4H1kTlXuwNuOj4c91wXQ/edit?usp=sharing");
            break;
        case 6:
            window.open("https://github.com/villeruokonen");
            break;

    }
}

